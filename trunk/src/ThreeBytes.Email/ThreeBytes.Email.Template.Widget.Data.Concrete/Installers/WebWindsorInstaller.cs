using Castle.Core;
using Castle.MicroKernel.Registration;
using Castle.Windsor;
using ThreeBytes.Core.Bootstrapper.Extensions.Windsor.Installers;
using ThreeBytes.Email.Template.Widget.Data.Abstract;
using ThreeBytes.Email.Template.Widget.Data.Abstract.Infrastructure;

namespace ThreeBytes.Email.Template.Widget.Data.Concrete.Installers
{
    public class WebWindsorInstaller : IWebWindsorRegistration
    {
        public void Install(IWindsorContainer container)
        {
            container.Register(
                AllTypes.FromThisAssembly().BasedOn<IEmailTemplateWidgetDatabaseFactory>().Configure(
                    component =>
                    {
                        component.Named(component.Implementation.Name);
                        component.LifeStyle.Is(LifestyleType.PerWebRequest);
                    }).WithService.Base(),
                AllTypes.FromThisAssembly().BasedOn<IEmailTemplateWidgetUnitOfWork>().Configure(
                    component =>
                    {
                        component.Named(component.Implementation.Name);
                        component.LifeStyle.Is(LifestyleType.PerWebRequest);
                    }).WithService.Base(),
                AllTypes.FromThisAssembly().BasedOn<IEmailTemplateWidgetTemplateRepository>().Configure(
                    component =>
                    {
                        component.Named(component.Implementation.Name);
                        component.LifeStyle.Is(LifestyleType.PerWebRequest);
                    }).WithService.Base(),
                AllTypes.FromThisAssembly().BasedOn<IProvideEmailTemplateWidgetSessionFactoryInitialisation>().Configure(
                    component =>
                        {
                            component.Named(component.Implementation.Name);
                            component.LifeStyle.Is(LifestyleType.PerWebRequest);
                        }).WithService.Base()
            );
        }
    }
}
